package Ejericios.VariablesOperadores;

public class Ej2 {
    public static void main(String[] args) {

        //Creamos las 2 variables numericas
        Integer num1 =12;
        Integer num2= 4;

        //Hacemos las opraciones de suma,resta, multiplicacion y division
        Integer suma= num1+num2;
        Integer resta= num1-num2;
        Integer mult= num1*num2;
        Integer div= num1/num2;

        //Finalmente lo imprimimos por pantalla
        System.out.println(suma);
        System.out.println(resta);
        System.out.println(mult);
        System.out.println(div);

    }
}
